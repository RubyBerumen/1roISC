
public class Ejercicio04_Figura {

	public static void main(String[] args) {
		
		System.out.println("==Programa que muestra un triangulo==");
		System.out.println();
		
		for(int i=0; i<10; i=i+1) {
			for(int x=i; x>=0; x--) {
				System.out.print(x);
			}
			System.out.println();
		}
		
		


	}

}
