
public class Ejercicio02_Figura_0_a_9 {

	public static void main(String[] args) {
		
		System.out.println("==Programa que muestra un triangulo de 0 a 9==");
		System.out.println();
		
		for(int i=0; i<=9; i=i+1) {
			for(int x=0; x<=0+i; x=x+1) {
				System.out.print(x);
			}
			System.out.println();
		}
		
		
		
		
		


	}

}
